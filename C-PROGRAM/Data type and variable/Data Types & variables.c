#include <stdio.h>

int main() {
    // Integer variable
    int age = 20;  
    printf("Age:- %d\n", age);

    // Float variable (single precision)
    float price = 99.50;  
    printf("Price:- %f\n", price);

    // Character variable
    char grade = 'A';  
    printf("Grade:-- %c\n", grade);

    // Double variable (double precision)
    double pi = 3.14159265359;  
    printf("Value of Pi:- %f\n", pi);

    return 0;
}

